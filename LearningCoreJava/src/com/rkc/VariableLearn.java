package com.rkc;

public class VariableLearn {
	
	public static void main(String[] args)
	{
		
		//int, float, double and long
		/*
		 * long l = 5000000000L; // 8 bytes int i = 5; // 4 bytes - 32 bits - >
		 * -2,147,483,648 to 2,147,483,648 short s = 5; // 2bytes -> 16 bits -> -32768
		 * to 32768 byte b = 5; // 1 byte -> 8 bits -> -128 to 127 float f = 5.5f; // 4
		 * bytes double d = 5.5;
		 * 
		 * char c = 'A'; c = 66; System.out.println(c);
		 */
		
		
		double d1 = 5; //implicit conversion
	//	int k = (int)5.6; //typecasting
		System.out.println(d1);
	}

}


